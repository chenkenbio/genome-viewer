fn save_token_to_user_config(token: &str) -> Result<()> {
    let config_path = expand_path("~/.config/genome_viewer/config.yaml")?;
    if let Some(parent) = config_path.parent() {
        std::fs::create_dir_all(parent)
            .with_context(|| format!("failed to create {}", parent.display()))?;
    }
    // Read existing config, update token, write back
    let mut config: serde_yaml::Value = if config_path.exists() {
        let text = std::fs::read_to_string(&config_path)?;
        serde_yaml::from_str(&text).unwrap_or(serde_yaml::Value::Mapping(Default::default()))
    } else {
        serde_yaml::Value::Mapping(Default::default())
    };
    config
        .as_mapping_mut()
        .context("user config is not a YAML mapping")?
        .insert(
            serde_yaml::Value::String("token".to_string()),
            serde_yaml::Value::String(token.to_string()),
        );
    let yaml = serde_yaml::to_string(&config)?;
    // Write with owner-only permissions (0600) to protect the token
    {
        use std::os::unix::fs::OpenOptionsExt;
        let mut file = std::fs::OpenOptions::new()
            .write(true)
            .create(true)
            .truncate(true)
            .mode(0o600)
            .open(&config_path)
            .with_context(|| format!("failed to write {}", config_path.display()))?;
        std::io::Write::write_all(&mut file, yaml.as_bytes())?;
    }
    // Enforce permissions even if the file already existed
    use std::os::unix::fs::PermissionsExt;
    std::fs::set_permissions(&config_path, std::fs::Permissions::from_mode(0o600))
        .with_context(|| format!("failed to set permissions on {}", config_path.display()))?;
    Ok(())
}

fn constant_time_eq(a: &str, b: &str) -> bool {
    use subtle::{Choice, ConstantTimeEq};
    // Compare in constant time regardless of length difference.
    // XOR the lengths first (leaks nothing about content), then compare
    // padded to the longer length so timing is uniform.
    let len_eq: Choice = a.len().ct_eq(&b.len());
    let max_len = a.len().max(b.len()).max(1);
    let mut result = len_eq;
    for i in 0..max_len {
        let byte_a = a.as_bytes().get(i).copied().unwrap_or(0);
        let byte_b = b.as_bytes().get(i).copied().unwrap_or(0);
        result &= byte_a.ct_eq(&byte_b) | !len_eq;
    }
    (result & len_eq).into()
}

fn generate_token() -> String {
    let mut bytes = [0u8; 16];
    getrandom::getrandom(&mut bytes).expect("failed to generate random token");
    bytes.iter().map(|b| format!("{:02x}", b)).collect()
}

fn display_url(addr: &SocketAddr) -> String {
    use std::net::IpAddr;
    let host = match addr.ip() {
        IpAddr::V4(ip) if ip.is_unspecified() => "localhost",
        IpAddr::V6(ip) if ip.is_unspecified() => "localhost",
        ref ip => return format!("http://{}:{}/", ip, addr.port()),
    };
    format!("http://{}:{}/", host, addr.port())
}

/// Get all non-loopback IPv4 addresses on this machine.
fn network_ips() -> Vec<std::net::IpAddr> {
    let output = match std::process::Command::new("hostname").arg("-I").output() {
        Ok(o) if o.status.success() => o,
        _ => return Vec::new(),
    };
    String::from_utf8_lossy(&output.stdout)
        .split_whitespace()
        .filter_map(|s| s.parse::<std::net::IpAddr>().ok())
        .filter(|ip| !ip.is_loopback())
        .collect()
}

