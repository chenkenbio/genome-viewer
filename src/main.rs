#[derive(Debug, Parser)]
#[command(
    name = "genome_viewer",
    version,
    about = "Genome browser server. Launch in any directory to browse genomic tracks."
)]
struct Args {
    /// JSON config file for pre-defined tracks (optional).
    /// Can be a full config or just `{ "tracks": [...] }`.
    #[arg(long)]
    config: Option<String>,

    /// Address to bind the server. Use port 0 to enable random port selection.
    #[arg(long, default_value = "0.0.0.0:0")]
    bind: String,

    /// Port or inclusive port range (for example: 9000 or 50000-60000).
    #[arg(short, long)]
    port: Option<String>,

    /// Genome name (default: hg38). Also selects the active genome when --config defines multiple genomes.
    #[arg(long)]
    genome: Option<String>,

    /// Path or URL to chromosome sizes file. Fetched from UCSC if omitted.
    #[arg(long)]
    chrom_sizes: Option<String>,

    /// Access token (auto-generated by default). Use --token VALUE to set explicitly.
    #[arg(long, default_missing_value = "auto", num_args = 0..=1)]
    token: Option<String>,

    /// Disable token authentication.
    #[arg(long)]
    no_token: bool,

    /// Generate a new token and save it to the user config.
    #[arg(long)]
    refresh_token: bool,

    /// Additional directories to allow browsing (repeatable).
    #[arg(long = "root")]
    roots: Vec<String>,

    /// Don't include the current directory as an allowed root.
    #[arg(long)]
    no_cwd: bool,

    /// Title for the viewer.
    #[arg(long)]
    title: Option<String>,
}

#[derive(Clone)]
struct AppState {
    config: Arc<ResolvedConfig>,
    tracks: Arc<RwLock<std::collections::HashMap<String, TrackConfig>>>,
    track_order: Arc<RwLock<Vec<String>>>,
    text_tracks: Arc<RwLock<std::collections::HashMap<String, TextTrack>>>,
    allowed_roots: Arc<Vec<PathBuf>>,
    token: Option<String>,
}

#[derive(Debug)]
struct AppError {
    status: StatusCode,
    message: String,
}

impl AppError {
    fn bad_request(message: impl Into<String>) -> Self {
        Self {
            status: StatusCode::BAD_REQUEST,
            message: message.into(),
        }
    }

    fn not_found(message: impl Into<String>) -> Self {
        Self {
            status: StatusCode::NOT_FOUND,
            message: message.into(),
        }
    }
}

impl From<anyhow::Error> for AppError {
    fn from(value: anyhow::Error) -> Self {
        Self {
            status: StatusCode::INTERNAL_SERVER_ERROR,
            message: value.to_string(),
        }
    }
}

impl IntoResponse for AppError {
    fn into_response(self) -> Response {
        if self.status.is_server_error() {
            tracing::error!(message = %self.message, "internal error");
            (self.status, "internal server error").into_response()
        } else {
            (self.status, self.message).into_response()
        }
    }
}

